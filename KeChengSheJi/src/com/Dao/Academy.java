package com.Dao;

public class Academy {
	private int academyId;
	private String name;
	public int getAcademyId(){
		return this.academyId;
	}
	public void setAcademyId(int academyId){
		this.academyId = academyId;
	}
	public String getName(){
		return this.name;
	}
	public void setName(String name){
		this.name = name;
	}
}
